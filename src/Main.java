/**
 * Main
 *
 * Entry point of the Hotel Booking application.
 * Demonstrates how a Java program starts execution and
 * prints basic information to the console.
 *
 * @author Dhruv
 * @version 1.0
 */

public class Main {

    /**
     * Main method – starting point of the Java application.
     * The JVM begins execution from this method.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("Welcome to the Hotel Booking System");

        // Application information
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: v1.0");

        // Program end message
        System.out.println("Application started successfully.");
    }
}